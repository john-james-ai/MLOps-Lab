    def test_read_all(self, container, caplog):
        start = datetime.now()
        logger.info(
            "\n\n\tStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        # ---------------------------------------------------------------------------------------- #
        dao = container.dao.profile()
        dtos = dao.read_all()
        assert len(dtos) == 5
        assert isinstance(dtos, dict)
        for i, dto in dtos.items():
            assert isinstance(dto, ProfileDTO)
            assert dto.id == i
            assert dto.name == f"profile_dto_{i}"
            assert dto.description == f"Description for Profile {i}"
            assert isinstance(dto.started, datetime)
            assert isinstance(dto.ended, datetime)
            assert dto.duration == i + 1000
            assert dto.user_cpu_time == i + 2000
            assert dto.percent_cpu_used == i + 3000
            assert dto.total_physical_memory == i + 4000
            assert dto.physical_memory_available == i + 5000
            assert dto.physical_memory_used == i + 6000
            assert dto.percent_physical_memory_used == i + 7000
            assert dto.active_memory_used == i + 8000
            assert dto.disk_usage == i + 9000
            assert dto.percent_disk_usage == i + 10000
            assert dto.read_count == i + 11000
            assert dto.write_count == i + 12000
            assert dto.read_bytes == i + 13000
            assert dto.write_bytes == i + 14000
            assert dto.read_time == i + 15000
            assert dto.write_time == i + 16000
            assert dto.bytes_sent == i + 17000
            assert dto.bytes_recv == i + 18000
            assert dto.task_id == i * 20
            assert isinstance(dto.created, datetime)
            assert isinstance(dto.modified, datetime)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\n\tCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )

