        assert isinstance(dtos, dict)
        for i, dto in dtos.items():
            assert isinstance(dto, DatasetDTO)
            assert dto.id == i
            assert dto.version == 1
            assert dto.cost == 1000 * i
            assert dto.nrows == 100 * i
            assert dto.ncols == i
            assert dto.null_counts == i + i
            assert dto.task_id == i + i
