            assert isinstance(dto, JobDTO)
            assert dto.id == i
            assert dto.name == f"job_{i}"
            assert dto.description == f"Description for Job # {i}"
            assert dto.pipeline == f"pipeline_{i}"
            assert dto.workspace == "test"
