    def get_filepath(self, dataset: Dataset) -> str:
        """Formats a filename using the Dataset name, environment, stage, and version."""
        filename = (
            dataset.name
            + "_"
            + dataset.env
            + "_"
            + dataset.stage
            + "_v"
            + str(dataset.version)
            + ".pkl"
        )
        return os.path.join("tests/data/movielens20m/repo", dataset.env, dataset.stage, filename)

