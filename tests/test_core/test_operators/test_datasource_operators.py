import inspect
from datetime import datetime
import pytest
import logging

from recsys.core.entity.datasource import DataSource, DataSourceURL

# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.source_builder
class TestDataSourceBuilder:  # pragma: no cover
    # ============================================================================================ #
    def test_movielens(self, container, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        builder = container.datasources.movielens()
        movielens = builder.execute()

        assert isinstance(movielens, DataSource)
        assert movielens.name == 'movielens25m'

        urls = movielens.get_urls()
        for url in urls.values():
            assert isinstance(url, DataSourceURL)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)
